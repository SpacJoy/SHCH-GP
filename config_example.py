# -*- coding: utf-8 -*-
"""
巴法云MQTT配置示例
展示两种不同的登录方式
"""

# =============================================================================
# 方式1：私钥登录（推荐）
# 只需要在巴法云控制台获取私钥，填入client_id即可
# =============================================================================

MQTT_CONFIG_PRIVATE_KEY = {
    'broker': 'bemfa.com',
    'port': 9501,
    'client_id': 'abcd1234efgh5678ijkl',  # 这里填入你的巴法云私钥
    'username': '',                       # 私钥登录时留空
    'password': '',                       # 私钥登录时留空
    'keep_alive': 60,
    'use_private_key': True              # 启用私钥登录模式
}

# =============================================================================
# 方式2：传统账号密码登录
# 需要提供完整的客户端ID、用户名、密码
# =============================================================================

MQTT_CONFIG_TRADITIONAL = {
    'broker': 'bemfa.com',
    'port': 9501,
    'client_id': 'my_device_001',         # 客户端ID
    'username': 'my_username',            # 巴法云用户名
    'password': 'my_password',            # 巴法云密码
    'keep_alive': 60,
    'use_private_key': False             # 禁用私钥登录，使用账号密码
}

# =============================================================================
# 使用说明
# =============================================================================

"""
私钥登录方式的优势：
1. 配置简单：只需要一个私钥
2. 安全性高：无需暴露用户名密码
3. 管理方便：在巴法云控制台可以随时重新生成私钥

如何获取私钥：
1. 登录巴法云控制台 (https://bemfa.com/)
2. 进入 "开发者中心" 或 "API管理"
3. 找到 "私钥管理" 或 "Secret Key"
4. 复制私钥字符串

如何使用：
1. 将 MQTT_CONFIG_PRIVATE_KEY 或 MQTT_CONFIG_TRADITIONAL 的内容
2. 复制到 src/config.py 文件中的 MQTT_CONFIG 部分
3. 替换为你的实际配置信息

示例私钥格式：
- 通常是一个32位或更长的字符串
- 可能包含字母和数字的组合
- 例如：abcd1234efgh5678ijklmnop90123456
"""
